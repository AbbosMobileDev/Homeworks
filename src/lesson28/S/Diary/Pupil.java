package lesson28.S.Diary;

public class Pupil {
    private String pupilName;
    private int age;
    private int couse;

    public Pupil(String pupilName, int age, int couse) {
        this.pupilName = pupilName;
        this.age = age;
        this.couse = couse;
    }

    public String getPupilName() {
        return pupilName;
    }


    public int getAge() {
        return age;
    }


    public int getCouse() {
        return couse;
    }


}
